#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*dst;
	int		i;	
	size_t	len;

	i = 0;
	len = ft_strlen(s) + 1;
	dst = malloc(sizeof(char) * len);
	if (!dst)
		return (0);
	while (s[i])
	{
		dst[i] = f(i, s[i]);
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
